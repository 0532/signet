package com.baidu.stqa.signet.extend.bo;

/**
 * @author suhanyuan
 * @version 3.0.0.0
 */

public class Project {

    private Long id;

    private String name;

    private String descr;

    public Project() {

    }

    public Project(Long id, String name, String descr) {
        this.id = id;
        this.name = name;
        this.descr = descr;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }

}
